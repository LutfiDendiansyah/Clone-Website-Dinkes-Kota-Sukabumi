          <h3>MISI</h3>
          <p>
            Mewujudkan sumber daya manusia yang beriman, bertaqwa dan berbudaya Mewujudkan pelayanan pendidikan yang berkualitas Mewujudkan pelayanan kesehatan yang berkualitas Mewujudkan pengembangan perdagangan dan sektor lapangan usaha
            lainnya yang berdaya saing tinggi Mewujudkan tata kelola pemerintahan yang baik dengan aparatur pemerintahan daerah yang profesional dan amanah Mewujudkan Kota Sukabumi yang nyaman dan indah
