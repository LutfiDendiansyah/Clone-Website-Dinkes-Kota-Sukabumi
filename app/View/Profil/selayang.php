          <p>
            Kota Sukabumi terletak pada bagian selatan tengah Jawa barat pada koordinat 106o 45’ 50’’ Bujur Timur dan 106o 45’ 10’’ Bujur Timur, 6o 49’ 29’’ Lintang Selatan dan 6o 50’ 44’’ Lintang Selatan, terletak di kaki Gunung Gede dan
            Gunung Pangrango yang ketinggiannya 584 m diatas permukaan laut, dengan suhu maksimum 29 oC yang berjarak 120 Km dari Ibukota Negara (Jakarta) dan 96 Km dari Ibukota Propinsi (Bandung) dengan luas wilayah 4.800,231 Ha. Memiliki
            penduduk sampai akhir Tahun 2002 tercatat 269.142 jiwa, dengan kepadatan penduduk rata-rata 50 jiwa/Km2 yang tersebar.
          </p>
          <p>
            Wilayah Kota Sukabumi seluruhnya berbatasan dengan wilayah Kabupaten Sukabumi yakni: di Sebelah Utara berbatasan dengan Kecamatan Cisaat dan Kecamatan Sukabumi Kabupaten Sukabumi, Sebelah Selatan dengan Kecamatan Nyalindung
            Kabupaten Sukabumi, Sebelah Barat dengan Kecamatan Cisaat Kabupaten Sukabumi, Sebelah Timur dengan Kecamatan Sukaraja Kabupaten Sukabumi.
          </p>
          <h2>SEJARAH</h2>
          <p>
            Kota Sukabumi berasal dari bahasa Sunda, yaitu Suka-bumen menurut keterangan mengingat udaranya yang sejuk dan nyaman, mereka yang datang ke daerah ini tidak ingin pindah lagi, karena suka atau senang bumen-bumen atau bertempat
            tinggal di daerah ini.
          </p>
          <p>
            Pada tahun 1914 Pemerintah Hindia Belanda menjadikan Kota Sukabumi sebagai "Burgerlijjk Bestuur" dengan status "Gemeenteraad Van Sukabumi" dengan alasan bahwa di Kota ini banyak berdiam orang-orang Belanda dan Eropa pemilik
            perkebunan-perkebunan yang berada di daerah Kabupaten Sukabumi bagian Selatan yang harus mendapatkan pelayanan yang istimewa.
          </p>
          <p>
            Sejak ditetapkannya Sukabumi menjadi Daerah Otonom pada bulan Mei 1926, maka resmi diangkat "Burgemeester" yaitu: Mr. GF.Rambonnet. Pada masa inilah dibangun sarana dan prasarana penting seperti Stasiun Kereta Api, Mesjid Agung,
            Gereja dan Pembangkit Listrik.
          </p>
